        <h1>Hotel Transylvania 2</h1>
        <img src="../img/hotel2.jpg" class="float-lg-end">
            <p>Count Dracula tak hanya menjadi ayah Mavis dan mertua Jonathan Loughran saja, sekarang dia adalah kakek dari Dennisovich Loughran alias Dennis. 
                Jika di film pertama, Drac lah yang overprotektif pada anaknya, kali ini Mavis melakukan tindakan yang cukup mirip pada Dennis.</p>

            <p>Dalam tujuh tahun berlalu ternyata banyak yang berubah di dunia monster dan manusia. Tak ada lagi ketakutan yang dialami oleh dua sisi, 
                semuanya berbaur dengan baik. Hotel Transylvania dibuka untuk umum, tak hanya monster tapi juga manusia. Mavis dan Johnny menikah di hotel 
                dan seluruh keluarga Johnny datang. Walaupun masih perlu beradaptasi dengan segala perbedaan yang ada, setidaknya tak ada pertengkaran yang terjadi.</p>
                
            <p>Hotel Transylvania selain jadi menerima tamu manusia juga mengalami perombakan lain yang diprakarsai Johnny. Sebagai manusia <i>kekinian,</i> 
                Johnny meng-<i>install</i> komputer dan segala macamnya untuk mempermudah sistem <i>booking</i> hotel. Dia juga mengajari para monster media sosial, 
                termasuk Drac yang ketinggalan zaman ribuan tahun lamanya.</p>

            <p>Tak lama setelah itu, Mavis memberi kabar gembira pada ayahnya kalau dia sedang hamil. Waktu di percepat dan Dennis pun lahir dengan sehat 
                dan dicintai semua monster juga keluarga manusianya. Hari demi hari berlalu, ulang tahun Dennis yang ke-5 semakin dekat. Tapi, Dennis yang nampak 
                sangat manusiawi belum juga memiliki taring. Itu membuat Drac khawatir kalau cucunya mungkin 100% manusia tulen walaupun memiliki ibu vampir.</p>

            <p>Di sisi lain, Mavis semakin menjadi ibu yang protektif pada anaknya. Dia jadi lebih manusiawi karena ingin menjaga anaknya tetap aman. 
                Dia jadi ngeri sendiri melihat betapa bahayanya Hotel Transylvania untuk Dennis, sehingga berpikir untuk tinggal di rumah orang tua Johnny di California. 
                Sesungguhnya Johnny lebih senang tinggal di Transylvania, tapi dia tentu akan mengikuti kemauan Mavis apapun itu.</p>

            <p>Namun, Drac tentu tidak menyetujui jika anak dan cucunya tinggal di dunia manusia. Drac pun bekerja sama dengan Johnny untuk membuat Mavis 
                'betah tapi tidak terlalu betah' di California. Dennis pun ditinggal di hotel dan diasuh oleh kakeknya saja selama kedua orang tuanya pergi. 
                Nah, selagi Mavis jauh dari Dennis, Drac tak mengulur waktu lagi untuk melatih Dennis menjadi monster yang sesungguhnya. Lagi, dibantu oleh 
                sahabat terbaik Drac yaitu Frank, Wayne, Griffin, Murray, dan tambahan Blobby si monster jeli.</p>

            <p>Drac mengirim Dennis ke summer camp buat para vampir kecil, tapi ternyata di sana juga sudah mengalami banyak perubahan. Tempat itu jadi mengikuti 
                protokol keamanan ekstrim, sehingga Drac berpikir tak akan bisa mengeluarkan sisi monster dari cucunya yang dia pikir sebagai <i>'late fanger'</i> 
                atau vampir yang gigi taringnya tumbuh terlambat.</p>

            <p>Jadi, Drac menjatuhkan Dennis dari puncak menara dan berharap cucunya akan berubah jadi kelelawar seperti dirinya. Namun, Dennis tak kunjung 
                bertransformasi padahal dia sudah hampir sampai ke tanah. Drac pun menyadari kalau ini tak akan berhasil dan segera terbang menyelamatkan cucunya.</p>

            <p>Aksi berbahaya tersebut direkam oleh anak-anak <i>summer camp</i> dan viral di internet, yang tentunya sampai ke Mavis. Dengan marah, Mavis berubah 
                menjadi kelelawar dan membawa Johnny dengan kakinya. Sesampainya di Hotel Transylvania, Drac mengeles tapi Mavis memiliki bukti. Mavis menegur ayahnya 
                karena menempatkan Dennis dalam bahaya besar dan ketidakmampuannya untuk menerima jika cucunya hanyalah manusia biasa.</p>

            <p>Mavis sebenarnya tidak betah juga di California, tapi dia memutuskan untuk pindah ke sana setelah pesta ulang tahun Dennis yang ke-5. Kata-kata Mavis 
                sangat menohok Drac, karena memang betul Drac membiarkan manusia masuk ke hotelnya, tapi tidak ke hatinya.</p>
                
            <p>Tanpa sepengetahuan Drac, Mavis mengundang kakeknya, Vlad, ke ultah Dennis. Drac histeris karena dia tahu ayahnya jauh lebih human-phobic daripada 
                dirinya dulu. Dan, mengetahui sekarang Hotel Transylvania bisa dimasuki manusia, terlebih dia memiliki cucu dan cicit manusia, tentu akan membuat Vlad murka. 
                Drac pun memberti tahun Johnny agar para tamu manusia memakai kostum monster.</p>

            <p>Vlad menerima undangan pesta tepat waktu dan segera datang ke hotel bersama Bela, pelayannya yang berbentuk kelelawar raksasa (yang bisa berbicara, seram sekali). 
                Drac berusaha sebisa mungkin menjauhkan Vlad dari para manusia, apalagi cucunya. Tapi, tentu itu hal yang tak memungkinkan. Vlad berpikir cicitnya itu
                <i>late fanger</i> sama seperti Drac dulu.</p>

            <p>Seperti Drac sebelumnya, Vlad juga berpikir kalau rasa takut akan menumbuhkan taring Dennis. Dia pun menyulap Kakie the Cake Monster, kartun favorit Dennis, 
                berubah mengerikan. Tapi, Drac melindungi cucunya yang ketakutan dan mengungkapkan segalanya. Pada akhirnya, Vlad jadi tahu kalau cicitnya, cucu (menantu), 
                dan keluarga dari pihak mantu itu semua manusia. Tentu itu sangat membuat Vlad emosi.</p>

                
            <p>Namun, Drac kemudian menjelaskan kalau manusia sudah berubah, tak lagi memusuhi monster. Vlad sempat tidak menerima tapi mau bagaimana lagi, karena semua telah terjadi. 
                Selagi orang dewasa berdebat, tak ada yang menyadari kalau Dennis telah kabur dari hotel. Bersama satu-satunya putri Wayne, Winnie, yang naksir berat pada Dennis, 
                mereka berdua pergi ke hutan. Winnie mengajak Dennis ke rumah pohonnya untuk bersembunyi.</p>

            <p>Dua bocah itu kemudian diserang oleh Bela yang sangat human-phobic. Bela melukai Winnie dan mengancam akan menghancurkan hotel. Itu membuat kemarahan Dennis memuncak, 
                yang men-trigger gigi taringnya tumbuh dan kemampuan vampirnya muncul. Dennis yang sekarang telah memiliki kekuatan vampir melawan balik Bela.</p>

            <p>Drac, Johnny, Mavis, Dennis, keluarga Loughran, dan monster lainnya datang dan bekerja sama untuk mengalahkan antek-antek Bela. Kelelawar raksasa itu sangat frustasi 
                dan mencoba membunuh Johnny sendiri dengan pasak. Namun, Vlad menghentikan itu semua dan menyusutkan asistennya agar mejadi kelelawar kecil, 
                kemudian mengusir Bela pergi tanpa ragu.</p>
                
            <p>Drac pun memulai pidato kalau manusia dan monster telah hidup berdampingan dengan damai sekarang. Dan, karena sekarang Dennisovich telah terkonfirmasi 
                memiliki kekuatan vampir, maka mereka tak jadi pindah ke California.</p>
