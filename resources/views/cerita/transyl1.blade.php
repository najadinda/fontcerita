        <h1>Hotel Transylvania</h1>
        <img src="../img/hotel1.jpg" class="m-3">
        <p>Dari judulnya dan gambarnya sudah pasti ini film tentang drakula, makhluk fiktif penghisap darah. Adam
            Sandler berperan sebagai Count Dracula 'Drac', pemilik dan
            manajer Hotel Transylvania, dan juga ayah dari Mavis. Selena Gomez didapuk sebagai Mavis, 'remaja' putri
            vampir berusia 118 tahun.</p>

        <p>Seperti karma, Mavis selalu penasaran dengan manusia dan pada akhirnya jatuh cinta pada Johnny. Kemudian ada
            Andy Samberg menjadi pengisi suara Jonathan Loughran,
            satu-satunya manusia yang bisa menembus dan masuk ke Hotel Transylvania.</p>

        <p>Marah setelah kematian istrinya akibat dibantai manusia, Count Dracula membangun sebuah hotel besar bintang
            lima di Transylvania untuk melindungi putrinya, Mavis.
            Tapi, kemudian dia membuka hotel itu untuk semua monster, menjadi tempat liburan yang <i>human-free</i> dan
            <i>monster-friendly.</i> Teman-teman dekat Drac
            diantaranya ada Frank dan Eunice, mereka <i>frankenstein.</i>
        </p>

        <p>Kemudian ada Wayne dan Wanda, beserta ratusan anak-anak mereka, yang merupakan keluarga <i>Werewolf</i>.
            Murray si <i>Mummy</i>, dan yang paling menarik mungkin Griffin
            si <i>Invisible man</i>. Untuk ulang tahun putrinya yang beranjak dewasa (dalam usia drakula), yaitu 118
            tahun, Drac berjanji untuk mengizinkan Mavis keluar hotel
            untuk menjelajahi dunia manusia seperti yang selalu anaknya inginkan.</p>

        <p>Tetapi, ternyata Drac mencurangi Mavis. Karena, dia membuat kota palsu yang terdiri dari <i>bellhops
                zombie</i> (karyawan hotel-nya Drac) yang berpura-pura menjadi manusia.
            Dracula berencana membuat Mavis ketakutan agar rasa penasarannya terhadap manusia menghilang. Well, rencana
            Drac berhasil.</p>

        <p>Tapi, para zombie itu memikat (secara tak sengaja tentunya) seorang manusia yang kebetulan berada di daerah
            sana. Manusia itu adalah Jonathan 'Johnny' yang berusia 21 tahun.
            Dia adalah seorang <i>solo backpacker traveller</i> yang tak takut dengan apapun. Johnny mengikuti para
            zombie hingga masuk ke Hotel Transylvania. Untungnya, Drac lah yang
            pertama kali menyadari kehadiran manusia.</p>

        <p>Dengan panik, Drac menculik Johnny dan menyulapnya menjadi "Johnnystein" agar tidak ketahuan kalau dia
            manusia. Drac mengarang kalau Johnny adalah 'sepupu jauh' yang lahir
            dari lengan kanan Frank. Sayangnya, Dracula melupakan hal yang paling penting, yaitu menghindarkan Johnny
            dari anaknya.</p>

        <p>Dan, benar saja, Mavis tak sengaja bertemu dengan Johnnystein dan mengalami yang disebut <i>Zing</i>. Itu
            adalah bentuk tarikan magis yang menandakan kalau dua sejoli saling
            jatuh cinta, dan ikatan tersebut hanya terjadi sekali dalam seumur hidup monster.</p>

        <p>Berarti, Mavis tidak akan bisa jatuh cinta lagi pada makhluk lainnya. Tentu itu mimpi indah (karena di dunia
            monster mimpi buruk = bagus) untuk Drac.</p>

        <p>Karena tak bisa lagi begitu saja mengusir sang manusia dari hotel, Drac improvisasi lagi kalau Johnnystein
            adalah <i>party organizer</i> ulang tahun Mavis. Sebagai manusia masa kini,
            Johnatan merasa acara yang disiapkan Drac sangat tidak seru. Dia pun membuat beberapa perubahan dan membuat
            semua monster bahagia terutama Mavis. Tapi, tentu saja Drac si kolot
            tak bisa menerima perubahan begitu cepat.</p>

        <p>Drac mencoba mengusir Johnny, tapi ketahuan oleh Mavis yang membawa manusia itu kembali ke dalam. Johnny
            mengajak Mavis melihat matahari terbit di atap hotel. <i>Sebagai dracula</i>,
            itu adalah pertama kalinya Mavis melihat matahari. Hal ini semakin membuat Mavis ingin pergi ke dunia
            manusia.</p>

        <p>Setiap film animasi selalu memiliki antagonis. Dalam film ini, dia adalah koki hotel Quasimodo, dan tikus
            peliharaannya, Esmeralda.</p>

        <p>Quasimodo mengompori monster-monster kalau Johnny adalah manusia, namun tentu tak ada monster yang percaya
            karena perawakan Johhnystein benar-benar meyakinkan. Quasimodo dan Esmeralda
            pun menculik Johnny diam-diam, dalam rangka untuk memasaknya jadi makanan. Tapi, Johnny berhasil
            diselamatkan Drac, dan dia malah membekukan Quasimodo untuk menjaga rahasia ini.</p>

        <p>Drac pun terbuka pada Johnny tentang kejadian yang membuatnya (dan monster-monster lainnya) menghindari
            manusia. Saat Mavis masih bayi, manusia memberondong datang ke kediaman mereka
            dan berhasil membunuh istri Drac.</p>

        <p>Itulah yang membuat Drac sangat membenci manusia dan jadi <i>overprotective</i> pada Mavis. Johnny merasa
            paham dengan perasaan Drac, dan rela pergi dari kehidupan para monster
            selama-lamanya.</p>

        <p>Tapi, Drac meminta Johnny tinggal setidaknya hingga pesta ultah Mavis selesai agar putrinya tidak terlalu
            sedih. Saat malam hari, Mavis dan Johnny berbagi ciuman dan membuat Drac marah.</p>

        <p>Dia tak sengaja mengaku telah menipu Mavis tentang kota manusia yang dia kunjungi. Kemudian, Quasimodo yang
            masih beku berhasil menerobos ke pesta dan Fly (monster lalat) menerjemahkan
            bahasa isyarat Quasimodo.</p>

        <p>Bisa ditebak kalau Quasimodo mengungkapkan jati diri Johnny yang sebenarnya, membuat kekacauan bagi semua
            monster kecuali Mavis yang masih tetap ingin bersama Johhny walau
            mereka berbeda dunia. Tapi, Johnny malah menghindar karena ingin menghormati Drac. Dia pun pergi dari Hotel
            Transylvania dan membuat Mavis membenci ayahnya.</p>

        <p>Drac menyadari kalau Mavis dan Johnny memiliki Zing, seperti yang dia miliki dengan istrinya. Drac meminta
            maaf pada Mavis dan juga para monster.</p>

        <p>Dia mengaku menakut-nakuti monster terhadap betapa bahayanya manusia, tidak menyadari kalau manusia sudah
            berevolusi dan berubah pandangannya tentang monster. Drac pun mengajak
            teman-teman baiknya yaitu Frank, Wayne, Griffin, dan Murray untuk menemukan Jonathan.</p>

        <p>Saat para monster pergi menuju bandara, ada Festival Transylvania yang mengagungkan dan menghormati monster.
            Alih-alih ketakutan karena kedatangan monster sungguhan, para manusia
            malah sangat senang dan membantu para monster selamat sampai di bandara.</p>

        <p>Sayangnya, pesawat Johnny sudah lepas landas. Tak peduli dengan sinar matahari yang bisa membunuhnya, Drac
            berubah jadi kelelawar dan mengejar pesawat.</p>

        <p>Drac berhasil meminta maaf dan membuat pesawat Johnny kembali ke bandara Transylvania. Drac membawa Johnny
            kembali pada Mavis, dan mereka pun dapat meresmikan Zing yang mereka rasakan.
            Drac menyadari kalau anak gadisnya telah beranjak dewasa dan berhak membuat keputusannya sendiri. Para
            monster (dan Johnny) pun lanjut berpesta.</p>
