            <p>Kembali pada tahun 1897, Dracula dan teman-temannya melakukan perjalanan dengan penyamaran menggunakan kereta api ke Budapest. 
                Namun, musuh bebuyutan Dracula (Profesor Abraham Van Helsing) naik kereta dan mengungkap monster; monster melarikan diri dengan 
                memanjat atap, dan Drakula mendorong teman-temannya turun dari kereta demi keselamatan mereka. Van Helsing menjadi terobsesi 
                untuk menghancurkan Drakula, tetapi terus-menerus diakali olehnya.</p>

            <p>Saat ini, satu tahun setelah film kedua, Drac menjalankan bisnis hotelnya dengan lancar bersama Mavis dan Johnny. Drac tertekan 
                karena dia tetap melajang sejak kematian istrinya Martha meskipun dia berusaha untuk bertemu seseorang. Salah mengartikan ini 
                sebagai stres karena terlalu banyak bekerja, Mavis memesan kapal pesiar agar mereka semua dapat beristirahat dan menghabiskan 
                lebih banyak waktu bersama sebagai satu keluarga.</p>
                
            <p>Drac, Johnny, Mavis, Dennis, Vlad, dan tamu hotel menaiki kapal pesiar bernama Legacy. Drac melihat kapten manusia kapal, Ericka, 
                dan jatuh cinta padanya pada pandangan pertama, sesuatu yang menurutnya tidak mungkin karena dia sudah "bersemangat" sebelumnya.</p>

            <p>Ericka kemudian pergi ke ruang pribadi dan rahasia di geladak bawah tempat dia bertemu dengan Abraham Van Helsing, diam-diam kakek buyutnya. 
                Van Helsing hampir seluruhnya menggerakkan tubuhnya untuk menghindari kematian, dan memiliki rencana untuk melenyapkan semua monster: 
                saat kapal pesiar tiba di kota Atlantis yang hilang, dia akan menggunakan Instrumen Penghancur di reruntuhan Atlantis.</p>
                
            <p>Van Helsing membuat Ericka berjanji untuk tidak membunuh Drakula sebelumnya, tetapi dia tetap melakukan upaya yang gagal berulang kali 
                untuk melakukannya. Teman-teman Drac mendengar Ericka mengeluh tentang ketidakmampuannya untuk mendapatkannya dan salah mengartikan ini s
                ebagai tanda kasih sayang. Drac dengan gugup mengajak Ericka berkencan, dan dia menerima karena dia melihat ini sebagai kesempatan lain 
                untuk membunuhnya.</p>

            <p>Saat mereka makan di pulau terpencil, Ericka tiba-tiba mulai jatuh cinta dengan Drac, setelah mereka mengetahui masa lalu satu sama lain 
                di mana mereka kehilangan orang yang mereka cintai - untuk Drac, itu adalah istri tercintanya Martha dan untuk Ericka, itu adalah orang tuanya.</p>

            <p>Mavis mengetahui bahwa ayahnya tertarik pada Ericka dan menjadi curiga terhadap motif Ericka. Kapal pesiar mencapai Atlantis, yang telah diubah 
                menjadi kasino. Drac memutuskan untuk memberi tahu Mavis yang sebenarnya tentang Ericka, tetapi terganggu melihat Ericka memasuki ruang bawah tanah. 
                Drac mengikutinya, dengan Mavis tidak jauh di belakang, dan mengetahui bahwa Ericka mengincar "pusaka keluarga".</p>

            <p>Dengan bantuan Drakula, dia menghindari jebakan di sekitar objek dan kabur. Mavis datang dan menghadapi mereka, dan Dracula mengaku bahwa dia 
                <i>Zing</i> dengan Ericka, yang membuat Mavis terkejut dan bingung. Setelah Ericka diberi tahu apa itu <i>Zing</i>, rasa bersalah batinnya karena 
                berbohong kepada Drac memaksanya untuk menolak perasaannya terhadapnya, membuat Drac patah hati dan Mavis merasa bersalah.</p>

            <p>Ericka yang menyesal memberi Van Helsing Instrumen Penghancur dan dia memasang jebakan untuk monster di pesta dansa. Menyadari bahwa Drac 
                masih tertekan tentang Ericka, Mavis meminta nasihat dari Johnny dan memberi tahu ayahnya untuk berbicara dengan Ericka, mengakui bahwa 
                dia takut dia meninggalkannya, yang menghilangkan ketakutan Drac.</p>

            <p>Van Helsing muncul dan mendorong DJ menjauh dan Ericka yang sedih terpaksa mengungkapkan bahwa dia adalah cicit perempuannya. 
                Van Helsing memperkenalkan Instrumen Kehancuran—kotak untuk lembar catatan musik—dan memainkan lagu yang menggerakkan Kraken yang bersahabat
                tinggal di dekat pulau untuk menyerang monster.</p>

            <p>Drac mencoba menghentikan Kraken yang jahat, tetapi terluka. Ericka menyelamatkan Drac dari Kraken yang jahat dan memohon kepada kakek buyutnya 
                untuk menghentikan kehancuran, mengakui cintanya pada Drac. Ini membuat marah Van Helsing, dan dia menyerang mereka berdua.</p>

            <p>Untuk menenangkan Kraken yang jahat, Johnny membuka kit DJ portabel dan memainkan lagu-lagu positif untuk mengalahkan lagu Van Helsing.</p>

            <p>Saat memainkan "Macarena", Kraken santai dan bahagia untuk selamanya. Van Helsing tidak dapat melawan lagu tersebut saat manusia dan monster mulai menari, 
                termasuk lembaran musik, yang merobek dirinya berkeping-keping selama proses tersebut.</p>

            <p>Saat Van Helsing juga menari, dia tidak sengaja terpeleset dan jatuh, tapi Drac menyelamatkannya. Tersentuh oleh tindakan kebaikan, 
                Van Helsing meminta maaf kepada para monster dan memberikan pengembalian uang penuh kepada semua orang untuk pelayaran, sebelum mengirim mereka kembali.</p>

            <p>Kembali ke hotel, Drac melamar Ericka, yang kelu pada pertanyaan sebelum menerima.</p>
